class Comparasion {

    public static void main(String args[]) {
        int num1, num2;
        num1 = 8;
        num2 = 15;

        System.out.println("8 == 15 is " + (num1 == num2));
        System.out.println("8 != 15 is " + (num1 != num2));
        System.out.println("8 > 15 is " + (num1 > num2));
        System.out.println("8 < 15 is " + (num1 < num2));
        System.out.println("8 >= 15 is " + (num1 >= num2));
        System.out.println("8 <= 15 is " + (num1 <= num2));
    }
}
